void USART2_IRQHandler(void)                	//串口1中断服务程序
{
		u8 Res;
#ifdef OS_TICKS_PER_SEC	 	//如果时钟节拍数定义了,说明要使用ucosII了.
	OSIntEnter();    
#endif
	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
		{
		Res =USART_ReceiveData(USART2);//(USART2->DR);	//读取接收到的数据
		
		if((USART2_RX_STA&0x8000)==0)//接收未完成
			{
			if(USART2_RX_STA&0x4000)//接收到了0x0d
				{
				if(Res!=0x0a){USART2_RX_STA=m;USART2_RX_STA++;USART2_RX_BUF[USART2_RX_STA&0X3FFF]=Res;}//接收错误,重新开始
				else {USART2_RX_STA=0;gyro.x_low=USART2_RX_BUF[37];gyro.x_high=USART2_RX_BUF[38];gyro.y_low=USART2_RX_BUF[39];gyro.y_high=USART2_RX_BUF[40];gyro.z_low=USART2_RX_BUF[41];gyro.z_high=USART2_RX_BUF[42];m=0;}	//接收完成了 
				}
			else //还没收到0X0D
				{	
				if(Res==0x0d){USART2_RX_STA|=0x4000;m=USART2_RX_STA;}
				else
					{
					USART2_RX_BUF[USART2_RX_STA&0X3FFF]=Res ;
					USART2_RX_STA++;
					if(USART2_RX_STA>(USART_REC_LEN-1))USART2_RX_STA=0;//接收数据错误,重新开始接收	  
					}		 
				}
			}   		 
     } 
#ifdef OS_TICKS_PER_SEC	 	//如果时钟节拍数定义了,说明要使用ucosII了.
	OSIntExit();  											 
#endif
} 
