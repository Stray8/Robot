void Voice_Send(u8 mode)
{
	 u8 LEB[8]={0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07};
		u8 SM[8]={0xB3,0xB4,0xB5,0xB6,0xB7,0xB8,0xB9,0xBA};
		if(Flag.voice==1){
			USART_ClearFlag(USART3,USART_FLAG_TC);
			USART_SendData(USART3,0xAA);         //向串口1发送数据
			while(USART_GetFlagStatus(USART3,USART_FLAG_TC)!=SET);//等待发送结束
			USART_SendData(USART3,0x07);         //向串口1发送数据
			while(USART_GetFlagStatus(USART3,USART_FLAG_TC)!=SET);//等待发送结束
			USART_SendData(USART3,0x02);         //向串口1发送数据
			while(USART_GetFlagStatus(USART3,USART_FLAG_TC)!=SET);//等待发送结束
			USART_SendData(USART3,0x00);         //向串口1发送数据
			while(USART_GetFlagStatus(USART3,USART_FLAG_TC)!=SET);//等待发送结束			
			USART_SendData(USART3,LEB[mode]);         //向串口1发送数据
			while(USART_GetFlagStatus(USART3,USART_FLAG_TC)!=SET);//等待发送结束		
			USART_SendData(USART3,SM[mode]);         //向串口1发送数据
			while(USART_GetFlagStatus(USART3,USART_FLAG_TC)!=SET);//等待发送结束
			Flag.voice=0;
		}
}



